        En vertu des dispositions de l’article 98 intitulé « Fourniture d’informations et de cartographie » de la loi n° 2018-28 du 
        12 décembre 2018, portant Code des communications électroniques : 
        <blockquote class="blockquote text-center mx-4 font-italic">
            « Les opérateurs, les opérateurs d'infrastructures, les exploitants d'infrastructures alternatives et les exploitants d'infrastructures
            essentielles visés à l'Article 114.- communiquent à l'Autorité de régulation, dans les conditions, la périodicité et les formats 
            demandés par celle-ci, l'ensemble des informations pertinentes relatives à leur réseau de communications électroniques,
            leurs infrastructures passives et actives, leurs infrastructures alternatives et à toutes autres informations pertinentes
            exigées par l'Autorité de régulation. »
        </blockquote>
    </p>

    <p class="text-justify" style="font-size: 12pt">
        C’est dans ce cadre que l’Autorité a décidé de mettre en place la première version d’un site web de cartographie interactive. Ainsi, le consommateur
        tient à sa disposition, la localisation géographique des stations de base (BTS) des opérateurs, par technologie (2G, 3G et 4G). 
    </p>

    <p class="text-justify" style="font-size: 12pt">
        En outre, cette cartographie a l’ambition de stimuler la compétition en offrant des alternatives de choix au consommateur et d’inciter les opérateurs
        au partage d’infrastructures conformément à l’article 103 du Code des communications électroniques, en essayant, le moins possible, de dupliquer une 
        station existante. Aussi, les stations FH sont-elles disponibles sur la carte.
    </p>

    <p class="text-justify" style="font-size: 12pt">
